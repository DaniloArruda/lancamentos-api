CREATE TABLE pessoa (
	codigo BIGINT(20) PRIMARY KEY AUTO_INCREMENT,
    nome VARCHAR(50) NOT NULL,
    logradouro VARCHAR(45),
    numero VARCHAR(6),
    complemento VARCHAR(30),
    bairro VARCHAR(30),
    cep CHAR(8),
    cidade VARCHAR(30),
    estado CHAR(2),
    ativo BOOLEAN NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

